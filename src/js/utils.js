export function formatTimeAgo(dateString) {
  try {
    if (!dateString) return 'Just now';
    const diffMs = Date.now() - new Date(dateString).getTime();
    const mins = Math.floor(diffMs / 60000);
    if (mins < 1) return 'Just now';
    if (mins < 60) return `${mins} mins ago`;
    const hrs = Math.floor(mins / 60);
    return `${hrs} hours ago`;
  } catch {
    return 'Just now';
  }
}

export function validateUsername(name) {
  const trimmed = name.trim();
  if (!trimmed) return 'Username cannot be empty.';
  if (trimmed.length < 3 || trimmed.length > 25) return 'Username must be between 3 and 25 characters.';
  if (!/^[a-zA-Z0-9_]+$/.test(trimmed)) return 'Username can only contain letters, numbers, and underscores.';
  const blacklist = ['admin', 'administrator', 'moderator', 'system', 'null', 'undefined', 'anonymous', 'staff', 'support', 'owner'];
  if (blacklist.includes(trimmed.toLowerCase())) return `Username "${trimmed}" is reserved or blacklisted.`;
  return null;
}

export function escapeHtml(str) {
  if (str === null || str === undefined) return '';
  return String(str)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}

export function debounce(fn, ms) {
  let timer;
  return (...args) => {
    clearTimeout(timer);
    timer = setTimeout(() => fn(...args), ms);
  };
}

/** Moderate content via server API */
export async function moderateContent(text) {
  try {
    const res = await fetch('./api/gemini/moderate', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ text }),
    });
    if (!res.ok) return false;
    const data = await res.json();
    return !!data.flagged;
  } catch {
    return false;
  }
}

