export async function incrementStoryView(storyId) {
  if (!storyId) return;
  const now = Date.now();
  const trackerKey = 'harbor_story_views_tracker';
  let tracker = {};
  try {
    tracker = JSON.parse(localStorage.getItem(trackerKey) || '{}');
  } catch (e) {
    tracker = {};
  }
  
  const lastViewTime = tracker[storyId];
  if (lastViewTime && (now - lastViewTime < 30 * 60 * 1000)) {
    return;
  }
  
  tracker[storyId] = now;
  localStorage.setItem(trackerKey, JSON.stringify(tracker));
  
  try {
    const { db } = await import('./firebase.js');
    const { doc, updateDoc, increment } = await import('https://www.gstatic.com/firebasejs/11.6.0/firebase-firestore.js');
    const storyRef = doc(db, 'stories', storyId);
    await updateDoc(storyRef, { views: increment(1) });
  } catch (err) {
    console.warn('Failed to increment view count:', err);
  }
}

