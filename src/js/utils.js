export function passesGenderFilter(storyCategory, userGender, isAdmin = false) {
  if (isAdmin) return true;
  const cat = (storyCategory || '').toLowerCase().trim();
  const gender = (userGender || '').toLowerCase().trim();

  const isMenCategory = cat === 'men' || cat === 'man';
  const isWomenCategory = cat === 'women' || cat === 'woman';

  const isUserMan = (gender.includes('man') || gender.includes('men')) && !gender.includes('woman') && !gender.includes('women');
  const isUserWoman = gender.includes('woman') || gender.includes('women');

  if (isUserMan) {
    if (isWomenCategory) return false;
  }
  if (isUserWoman) {
    if (isMenCategory) return false;
  }
  if (!userGender) {
    if (isMenCategory || isWomenCategory) return false;
  }
  return true;
}

